               <p>我固执的认为，我是一只憧憬着的毛毛虫，等待着蜕变成美丽蝴蝶的那一瞬。于是，我在粗糙的树干上静默这守候。远处，那芬芳的花朵，在不起眼的角落，悄然绽放。</p>

               <p>春如旧，人空瘦，泪痕红疤宵浇透，在自己满怀着憧憬时，现实的忧愁却如同云雾般弥漫自己的世界。我无奈的发现，自己的成绩竟总得不到提高，自己的努力得不到认可，自己那所谓的希望，不过是一道微弱的火苗，承受不了一点风吹雨打。无奈，一怀愁绪，该去哪里倾诉？</p>

               <p>该放弃么？我攥紧满是冷汗的双手，母亲那期翼的目光忘记了么？父亲那静默的身影忘记了么？那与星辰共舞，与朝阳攀早的努力付诸东流了么？脑中拥挤而繁杂，像熙熙攘攘的火车站。</p>

               <p>猛然想起朋友曾对自己说过：“学习之路上，只要多下苦功夫并持之以恒，终能获得成功。”是啊，那“吃得苦中苦，方为人上人”的道理又何须再多讲？于是，我在人生路上有了毅力。</p>

               <p>“毛毛虫想到达河的彼岸，最好的方法是什么？”“那就变成蝴蝶吧！”</p>

               <p>我微笑着，就这样我走过绚烂的季节。</p>

               <p>告别明媚的春天时，我拖着下巴，了望窗外几近透明的天空。窗外，蒲公英随风飞扬。忽然想起一句话：心有多高，我们的世界就有多大。</p>

               <p>于是怀揣梦想，我继续前行。于是人生路上，我书写了一份满意的答卷。</p>

